    private void jButton_model_terren_ukazkaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_model_terren_ukazkaActionPerformed
        
        InternalFrameproject.Rozpätie.setZ(help.ReadCheckDouble(jTextField_Z,-1));
        InternalFrameproject.Rozpätie.setA(help.ReadCheckDouble(jTextField_A,-1));

        if(InternalFrameproject.Rozpätie.isAppTeren() == false){
            InternalFrameproject.Rozpätie.setLCcoordinates(0, 0, 0, InternalFrameproject.Rozpätie.getA(), 0);
            
        }else{
             InternalFrameproject.Rozpätie.setLCcoordinates(Double.valueOf(jTextField_X1.getText()), Double.valueOf(jTextField_L1.getText()),Double.valueOf(jTextField_Z1.getText()), Double.valueOf(jTextField_X2.getText()), Double.valueOf(jTextField_Z2.getText()));         
        }
        
        
        try {
            //InternalFrameproject.Rozpätie.
            InternalFrameproject.Rozpätie.generateTerrain();
            SurfaceGraph povrch = new SurfaceGraph(InternalFrameproject.Rozpätie.getTeren(),InternalFrameproject.Rozpätie.getTerenTriangulacia().getResultsPoinList(),true );
            povrch.SetNameAndLables(InternalFrameproject.Rozpätie.getMeno(),InternalFrameproject.Rozpätie.getMenoProjektu(), language_internal_frame.LangLabel(constants.getLanguage_option(), 2), language_internal_frame.LangLabel(constants.getLanguage_option(), 3), language_internal_frame.LangLabel(constants.getLanguage_option(), 4));
            povrch.setLC1LC2andCalculateheight(InternalFrameproject.Rozpätie.getLCcoordinates1(), InternalFrameproject.Rozpätie.getLCcoordinates2());
            povrch.Run();
        
        } catch (DelaunayError ex) {
            Logger.getLogger(BasicSettingsPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

        if(InternalFrameproject.Rozpätie.getTerenTriangulacia().isIsMeshCalculated() == true){
    
        }
    }//GEN-LAST:event_jButton_model_terren_ukazkaActionPerformed

